class HP_inter(object):
    """Data Structure for inter Helical parameters, including Shift, Slide, Rise, Tilt, Roll, Twist."""

    def __init__(self, shi, sli, ris, til, rol, twi):
        self.shi = shi
        self.sli = sli
        self.ris = ris
        self.til = til
        self.rol = rol
        self.twi = twi